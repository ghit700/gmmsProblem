
//打开二维码扫面界面
function fnScan(type, data, url) {
    api.openWin({
        name: 'scan',
        url: 'widget://html/home/scan/scan.html',
        rect: {
            x: 0,
            y: 0,
            w: 'auto',
            h: 'auto'
        },
        reload: true,
        pageParam: {
            data: data,
            type: type,
            url: url
        }
    });
}


